# Friday Shell
# Just a simple command-line tool to implement the F.R.I.D.A.Y Core


from friday_core import Friday_Core as core

def main():
    friday = core()

    while (True):
        msg = input("> ")
        if (msg == "/stop"):
            break
        output = friday.friday_core(msg)
        print(output)

if __name__ == "__main__":
    main()