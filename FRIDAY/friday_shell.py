# Friday Shell
# Just a simple command-line tool to implement the F.R.I.D.A.Y Core


import friday_core as core

def main():
    while (True):
        msg = input("> ")
        if (msg == "/stop"):
            break
        output = core.friday_core(msg)
        print(output)

if __name__ == "__main__":
    main()